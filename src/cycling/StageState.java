package cycling;

/**
 * This enum is used to represent the stage states on road races.
 * 
 * @author 730049785
 * @author 730099588
 * @version 1.0
 *
 */
public enum StageState {
	
	/**
	 * Used for when Checkpoints are being added and modified.
	 */
	PREPARING_STAGE,
	
	/**
	 * Used for when Checkpoints are finalised and when the Stage is accepting results.
	 */
	WAITING_FOR_RESULTS,
	
}
